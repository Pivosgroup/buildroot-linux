u_int16_t GB2Unicode(u_int16_t gbcode)
{
	int low, high, mid;
	
	low = 0; high = sizeof(GB2Uni_Map)/sizeof(GB2Uni_Map[0])-1;
	
	while (low <= high)
	{
		mid = (low + high) >> 1;
		if (GB2Uni_Map[mid][0] == gbcode) return GB2Uni_Map[mid][1];
		else if (GB2Uni_Map[mid][0] > gbcode) high = mid - 1;
		else low = mid + 1;
	}
	
	return 0; // Failed  
}

