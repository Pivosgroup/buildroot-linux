void StrGB2Unicode(char *dest, const char *src, int n)
{  
	u_int16_t 	code, tmp_code;
	int i;
	
	for (i = 0; i < n; i+=2)
	{  
		code = *(src+i)<<8 | *(src+i+1)&0x00FF;
		tmp_code = GB2Unicode(code);
		*(dest+i) = tmp_code >> 8;
		*(dest+i+1) = tmp_code & 0x00FF;
	}  
	*(dest+i)='\0';  
}

int StrnGB2Unicode(char *dest, const char *src, int dest_len, int src_len)
{
	int src_index = 0, dest_index = 0;
	u_int16_t 	code, *dest_str = (u_int16_t *)dest;
	dest_len = dest_len >> 1 ;
	
	while((src_index < src_len)&&(dest_index < dest_len))
	{  
		code = src[src_index++] ;
		if(code > 0x7f){ 
		    code = (code << 8) | src[src_index++];
		    code = GB2Unicode(code);
		}
		if(code)
		    dest_str[dest_index++] = code ;
		else
		    dest_str[dest_index++] = '_' ;
	}
	return (dest_index);
}

